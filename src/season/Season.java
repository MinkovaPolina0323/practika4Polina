package season;

    enum Season {
        // Создаем 4 константы - времена года с их температурой и названием
        WINTER(-5, "Зима"),      // Зима: температура -5, название "Зима"
        SPRING(15, "Весна"),     // Весна: температура 15, название "Весна"
        SUMMER(25, "Лето"),      // Лето: температура 25, название "Лето"
        AUTUMN(10, "Осень");     // Осень: температура 10, название "Осень"

        // Добавляем переменные для хранения температуры и названия
        private int avgTemp;     // Переменная для средней температуры
        private String name;     // Переменная для русского названия

        //  Создаем конструктор - метод, который вызывается при создании каждой константы
        // Конструктор устанавливает температуру и название для каждого времени года
        Season(int temp, String name) {
            this.avgTemp = temp;  // Сохраняем температуру в переменную avgTemp
            this.name = name;     // Сохраняем название в переменную name
        }

        // Метод для получения описания времени года
        public String getDescription() {
            // Проверяем: если это Лето (SUMMER)
            if (this == SUMMER) {
                return "Теплое время года";  // Для лета возвращаем "Теплое"
            } else {
                return "Холодное время года"; // Для остальных - "Холодное"
            }
        }

        // Метод для получения температуры
        public int getAvgTemp() {
            return avgTemp;  // Просто возвращаем значение температуры
        }

        // Метод для получения названия
        public String getName() {
            return name;     // Просто возвращаем название
        }
    }
